k_angka = [5, 2, 1, 2.5, 5, 9, 2, 1.5, 9, 6, 6, 3, 7, 8, 2]

nilai_terbesar = max(k_angka)
print("Elemen terbesar dalam list adalah:", nilai_terbesar)

nilai_rata_rata = sum(k_angka) / len(k_angka)
print("Nilai rata-rata dari list adalah:", nilai_rata_rata)

nilai_unik = list(set(k_angka))
print("Nilai item data yang unik dalam list adalah:", nilai_unik)